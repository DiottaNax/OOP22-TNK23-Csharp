namespace Tnk23Game.common
{
    /// <summary>
    /// The Directions enum represents different directions in a two-dimensional space.
    /// Each direction is associated with a velocity vector represented by the Vector2D class.
    /// </summary>
    public enum Directions
    {
        /// <summary>
        /// The north direction (up).
        /// </summary>
        NORTH,

        /// <summary>
        /// The south direction (down).
        /// </summary>
        SOUTH,

        /// <summary>
        /// The west direction (left).
        /// </summary>
        WEST,

        /// <summary>
        /// The east direction (right).
        /// </summary>
        EAST,

        /// <summary>
        /// No direction (stationary).
        /// </summary>
        NONE
    }
}
