namespace Tnk23Game.Events
{
    /// <summary>
    /// The WorldEventType enum represents the types of events that can occur within the game world.
    /// </summary>
    public enum IWorldEventType
    {
        /// <summary>
        /// Represents a shoot event.
        /// </summary>
        Shoot_event,

        /// <summary>
        /// Represents a spawn event.
        /// </summary>
        Spawn_event,

        /// <summary>
        /// Represents a death event.
        /// </summary>
        Death_event
    }
}
